        4. 最后以一句温暖鼓励结尾
        5. 语气自然，不要像官方通知
        """;

    public const string DailySummaryUser = "今天课程：\n{0}\n请生成放学总结。";

    // ========================================
    //  自然语言解析
    // ========================================

    public const string NLParseSystem = """
        你是一个时间解析助手。将用户的中文提醒输入解析为严格 JSON 格式。

        输出 JSON 格式（不要多余文字，只输出 JSON）：
        {
          "success": true/false,
          "error": "如果无法解析，填写失败原因（成功时省略此字段）",
          "type": "fixed_time" | "daily_repeat" | "subject_linked",
          "date": "yyyy-MM-dd" | null,
          "time": "HH:mm" | null,
          "subjectName": "科目名称" | null,
          "minutesBefore": 3,
          "content": "提醒正文"
        }

        字段说明：
        - type=fixed_time: 有明确日期+时间
        - type=daily_repeat: 每天重复的时间（不含日期）
        - type=subject_linked: 关联某节科目（如"数学课前"）
        - date: type=fixed_time 时必填
        - time: 24小时制
        - minutesBefore: type=subject_linked 时，提前多少分钟提醒（默认 3）
        - content: 提取用户真正想被提醒的事情（去掉"提醒我"等冗余词）

        时间理解规则：
        - "早上/上午" → 7:00-11:00 范围内的合理时间
        - "中午" → 12:00
        - "下午" → 14:00
        - "晚上" → 20:00
        - 未指定具体时间但指定了日期 → 默认 08:00
        - "明天" → 当前日期 +1 天
        - "后天" → 当前日期 +2 天
        """;

    public const string NLParseUser = """
        用户输入: {0}
        当前日期: {1}
        今天是: {2}
        """;
